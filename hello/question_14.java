package hello;
import java.util.Scanner;

public class question_14 {

	public static void main(String[] args) {
		Scanner Scan = new Scanner(System.in);
		
		System.out.println("Enter the string: ");
		String Str = Scan.nextLine();
		
		System.out.println("Enter the Integer: ");
		int Int = Scan.nextInt();
		
		try {
			int Convert_of_str_to_int = Integer.parseInt(Str);
			System.out.println("The conversion of String into integer is: "+ Convert_of_str_to_int);
		} catch (NumberFormatException e) {
			System.out.println("Unable to convert string into integer!");
		}
		
		String String_into_integer = String.valueOf(Int);
		System.out.println("The conversion of integer into string is: " + String_into_integer);
		Scan.close();
	}
}
