package week2;
import java.util.Scanner;

public class tutorial_2_question_7 {

	public static void main(String[] args) {
		Scanner Scan = new Scanner (System.in);
		
		System.out.println("Enter your first number ");
		int first_num = Scan.nextInt();
		
		System.out.println("Enter your second number: ");
		int second_num = Scan.nextInt();
		
		if (first_num > second_num) {
			System.out.println(first_num + " is biggest between"+ first_num + " and " + second_num);
		}
		else if (first_num == second_num) {
			System.out.println("Both number are same");
		}
		else {
			System.out.println(second_num + " is biggest between " + first_num + " and " + second_num);
		}
	}

}
