class Box{
	int width;
	int height;
	int depth;
	
	public Box(int length){
		this.width = length;
		this.height = length;
		this.depth = length;
	}
	
	public Box(int length, int breadth, int height) {
		this.width = length;
		this.height = breadth;
		this.depth = height;
	}
	
	public Box() {
		this.width = 10;
		this.height = 8;
		this.depth = 12;
	}
	
}